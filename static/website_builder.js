const builderForm = document.querySelector("#websiteBuilderForm");
const promptInput = document.querySelector("#websitePrompt");
const themeInput = document.querySelector("#websiteTheme");
const statusBadge = document.querySelector("#builderStatus");
const previewFrame = document.querySelector("#websitePreview");
const generatedCode = document.querySelector("#generatedCode");
const generateButton = document.querySelector("#generateWebsiteButton");
const regenerateButton = document.querySelector("#regenerateWebsiteButton");
const copyCodeButton = document.querySelector("#copyCodeButton");
const downloadFilesButton = document.querySelector("#downloadFilesButton");
const fileTabs = document.querySelectorAll(".file-tab");
const quickPromptButtons = document.querySelectorAll("[data-prompt]");

const defaultFiles = {
  "index.html": "<!-- Generated code will appear here. -->",
  "style.css": "/* Generated CSS will appear here. */",
  "script.js": "// Generated JavaScript will appear here."
};

let generatedFiles = { ...defaultFiles };
let activeFile = "index.html";
let lastPrompt = "";

function setBuilderStatus(message) {
  if (statusBadge) statusBadge.textContent = message;
}

function escapeHtml(text) {
  return String(text || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

function renderActiveFile() {
  if (!generatedCode) return;
  generatedCode.innerHTML = escapeHtml(generatedFiles[activeFile] || "");
}

function buildPreviewLocally(files) {
  return String(files["index.html"] || "")
    .replace(/<link[^>]+href=["']style\.css["'][^>]*>/i, `<style>${files["style.css"] || ""}</style>`)
    .replace(/<script[^>]+src=["']script\.js["'][^>]*>\s*<\/script>/i, `<script>${files["script.js"] || ""}<\/script>`);
}

async function updatePreview() {
  if (!previewFrame) return;
  try {
    const response = await fetch("/api/website-builder/preview", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ files: generatedFiles })
    });
    const data = await response.json();
    if (!response.ok) throw new Error(data.error || "Preview failed.");
    previewFrame.srcdoc = data.html;
  } catch (error) {
    previewFrame.srcdoc = buildPreviewLocally(generatedFiles);
  }
}

async function generateWebsite() {
  const prompt = promptInput.value.trim();
  if (!prompt) {
    setBuilderStatus("Add a prompt");
    promptInput.focus();
    return;
  }

  lastPrompt = prompt;
  setBuilderStatus("Generating");
  generateButton.disabled = true;
  regenerateButton.disabled = true;

  try {
    const response = await fetch("/api/website-builder/generate", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ prompt, theme: themeInput.value })
    });
    const data = await response.json();
    if (!response.ok) throw new Error(data.error || "Generation failed.");
    generatedFiles = data.files || { ...defaultFiles };
    setBuilderStatus(data.source === "groq" ? "Generated by Groq" : "Generated locally");
    renderActiveFile();
    await updatePreview();
  } catch (error) {
    setBuilderStatus(error?.message || "Generation failed");
  } finally {
    generateButton.disabled = false;
    regenerateButton.disabled = false;
  }
}

async function copyAllCode() {
  const bundle = ["index.html", "style.css", "script.js"]
    .map((fileName) => `/* ${fileName} */\n${generatedFiles[fileName] || ""}`)
    .join("\n\n");
  try {
    await navigator.clipboard.writeText(bundle);
    setBuilderStatus("Copied");
  } catch (error) {
    setBuilderStatus("Copy blocked");
  }
}

function downloadFile(fileName, content, mimeType) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = fileName;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function downloadGeneratedFiles() {
  downloadFile("index.html", generatedFiles["index.html"] || "", "text/html");
  downloadFile("style.css", generatedFiles["style.css"] || "", "text/css");
  downloadFile("script.js", generatedFiles["script.js"] || "", "text/javascript");
  setBuilderStatus("Downloads started");
}

builderForm?.addEventListener("submit", (event) => {
  event.preventDefault();
  generateWebsite();
});

regenerateButton?.addEventListener("click", () => {
  if (lastPrompt && !promptInput.value.trim()) promptInput.value = lastPrompt;
  generateWebsite();
});

copyCodeButton?.addEventListener("click", copyAllCode);
downloadFilesButton?.addEventListener("click", downloadGeneratedFiles);

fileTabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    activeFile = tab.dataset.file;
    fileTabs.forEach((item) => item.classList.toggle("is-active", item === tab));
    renderActiveFile();
  });
});

quickPromptButtons.forEach((button) => {
  button.addEventListener("click", () => {
    promptInput.value = button.dataset.prompt || "";
    promptInput.focus();
  });
});

promptInput.value = "Create a landing page for an AI startup.";
renderActiveFile();
updatePreview();
